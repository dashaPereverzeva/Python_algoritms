while True:
    number1 = int(input('Введите первое число: '))
    number2 = int(input('Введите втрое число: '))
    znak = input('Введите знак операции: ')
    if znak == '+':
        result = number1 + number2
        print (result)
    if znak == '-':
        result = number1 - number2
        print (result)
    if znak == '*':
        result = number1 * number2
        print (result)
    if znak == '/' and number2 != 0:
        result = number1 / number2
        print (result)
    if number2 == 0:
        print ('Делить на 0 нельзя!')
    if int(znak) == 0:
        break


